print("____________________________________________________")
DNA = input("Please enter DNA sequence: \n")
print("____________________________________________________")
print("DNA:", DNA,)

RNA = DNA.maketrans("AGCT", "AGCU")

rna = DNA.translate(RNA)

print("RNA:", DNA.translate(RNA))

